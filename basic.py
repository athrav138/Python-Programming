x = int(input("Enter first number: "))
y = int(input("Enter second number: "))

def add(a,b):
  return a+b

def subtract(a,b):
  return a-b

def multiply(a,b):
  return a*b

def division(a,b):
  return a/b

print("Addition:",add(x,y))
print("Subtraction:",subtract(x,y))
print("Multiplication:",multiply(x,y))
print("Division:",division(x,y))



